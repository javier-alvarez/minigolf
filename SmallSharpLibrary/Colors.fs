namespace Library

type Color = 
   struct
      val A:byte
      val R:byte
      val G:byte
      val B:byte
   end
   new (a,r,g,b) = { A=a; R=r; G=g; B=b }

module internal Brush =
    let From (color:Color) =
        System.Windows.Media.SolidColorBrush(
            System.Windows.Media.Color.FromArgb(color.A,color.R,color.G,color.B)
        )

